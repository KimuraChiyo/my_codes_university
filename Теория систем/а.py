from scipy.constants import c, G, h

speed_of_light = c  # Скорость света в вакууме
gravitational_constant = G  # Гравитационная постоянная
planck_constant = h  # Постоянная Планка

print("Значения физических констант:")
print("Скорость света в вакууме:", speed_of_light, "м/с")
print("Гравитационная постоянная:", gravitational_constant, "м^3/(кг*с^2)")
print("Постоянная Планка:", planck_constant, "Дж*с")


